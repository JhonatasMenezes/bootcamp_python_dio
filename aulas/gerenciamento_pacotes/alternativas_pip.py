## PIPENV
# Pipenv é uma alternativa ao pip e possui o gerenciamento de dependências
# das libs que instalamos, ou seja, se precisar excluir uma lib, ele consegue 
# excluir todas as libs que foram baixadas como dependência dessa lib que excluimos.


## POETRY
# Outra alternativa ao pip, porém, de forma mais robusta e completa. 
# Possui init de projeto interativo e bem intuitivo pra auxiliar no 
# gerenciamento das dependências do projeto. Os comandos são um pouco
# diferentes do pip/pipenv, mas são muito fáceis de se acostumar e usar.
